	  SUBROUTINE FCN (N, T, Y, YPRIME)
	  
      
      INTEGER  N
      REAL*8  T, Y(N), YPRIME(N), r, e, m
	  common/block/ e, m

	  !Y(1)-f, Y(2)-psi, Y(3)-phi, Y(4)-tau, Y(5)-mu
	  
	  r = T
	  !f'
	  YPRIME(1) = 0.1D1 / r - Y(1) / r - (Y(3) ** 2 * Y(1) + e ** 2 * Y(4) ** 2 * Y(2) ** 2 / &
      &Y(1) + Y(5) ** 2 * Y(2) ** 2 + m ** 2 * Y(2) ** 2) * r
	  
	  !phi'
	  YPRIME(2) =  -Y(3) / Y(1) / r * (1 - Y(5) ** 2 * Y(2) ** 2 * r ** 2 - &
      &m ** 2 * Y(2) ** 2 * r ** 2) - Y(3) / r - e ** 2 * Y(4) ** 2 * Y(2) / &
      &Y(1) ** 2 - Y(5) ** 2 * Y(2) / Y(1) + m ** 2 * Y(2) / Y(1)

	  !tau'
	  YPRIME(3) = Y(5) - Y(4) * Y(3) ** 2 * r - e ** 2 * Y(4) ** 3 * Y(2) ** 2 * r / &
      &Y(1) ** 2

	  !mu'
	  YPRIME(4) = -2 * Y(5) / r - 2 * Y(5) * Y(3) / (abs(Y(2)) +  1.0D-16) + e ** 2 * Y(4) / Y(1)

	  !psi'
	  YPRIME(5) = Y(3)


      RETURN
      END
	 
